package com.ezen.batis;

public class InsaDTO {
	int id;
	String jik;
	int fasu, gisu, hap;
	
	public InsaDTO() {}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getJik() {
		return jik;
	}

	public void setJik(String jik) {
		this.jik = jik;
	}

	public int getFasu() {
		return fasu;
	}

	public void setFasu(int fasu) {
		this.fasu = fasu;
	}

	public int getGisu() {
		return gisu;
	}

	public void setGisu(int gisu) {
		this.gisu = gisu;
	}
	
	public int getHap() {
		return hap;
	}

	public void setHap(int hap) {
		this.hap = hap;
	}

}
